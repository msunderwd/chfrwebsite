<p>This document provides the General and Special rules in force on the
Chestnut Hill &amp; Frost River Railroad.&nbsp;
All employees of the CH&FR shall be familiar with the contents of
these rules and timetables, and the conditions set forth shall be
followed at all times.&nbsp; Exceptions to these rules may only be
permitted by explicit order of the Train Master.&nbsp;</p> <p>
Timetable information may be overridden by specific Train Order or
Track warrant.</p>

